def get_translation(lang: str, key: str, default: str = "") -> str:
    """Получает перевод для ключа на указанном языке."""
    return TRANSLATIONS.get(lang, TRANSLATIONS["ru"]).get(key, default or key)

class TranslationDict(dict):
    """Словарь переводов с поддержкой dot-нотации для Jinja2."""
    def __getattr__(self, key):
        # Avoid infinite recursion for special attributes
        if key.startswith('_'):
            raise AttributeError(f"Attribute {key} not found")
        try:
            return self[key]
        except KeyError:
            # Provide helpful error message with available keys
            available_keys = ', '.join(list(self.keys())[:10])
            raise AttributeError(
                f"Translation key '{key}' not found. "
                f"Available keys (first 10): {available_keys}..."
            )
    
    def __setattr__(self, key, value):
        self[key] = value
    
    def __getstate__(self):
        return dict(self)
    
    def __setstate__(self, state):
        self.update(state)

def get_all_translations(lang: str) -> TranslationDict:
    """Возвращает все переводы для указанного языка с поддержкой dot-нотации."""
    translations = TRANSLATIONS.get(lang, TRANSLATIONS["ru"])
    return TranslationDict(translations)
